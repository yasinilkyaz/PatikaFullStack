function startTime() {
    const today = new Date();
    let h = today.getHours();
    let m = today.getMinutes();
    let s = today.getSeconds();
    let d = today.getDay();
    m = checkTime(m);
    s = checkTime(s);
    let getDay=dayName(d);
    document.getElementById('myClock').innerHTML =  h + ":" + m + ":" + s + " " + getDay ;
    setTimeout(startTime, 1000);
  }
  
  function checkTime(i) {
    if (i < 10) {i = "0" + i};  // add zero in front of numbers < 10
    return i;
  }
  function dayName(dayNumber){
    switch (dayNumber) {
    case 1:
        return "Pazartesi";
      case 2:
        return "Salı";
      case 3:
        return "Çarşamba";
      case 4:
        return "Perşembe";
      case 5:
        return "Cuma";
      case 6:
        return "Cumartesi";
      case 7:
        return "Pazar";
      default:
        return "Hatalı gün";
    }

  }